     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function loadd(Request $request)
    {
        $data=request()->except(['_token']);
        $res=Login::where('user_name',$data['user_name'])->first();
        if($data['user_pwd']!=$res['user_pwd']){
            return redirect("/jio/index");
        }
        if($res){
            session(['res'=>$res]);
            return redirect("/jineng/index");
        }else{
            return redirect("/jio/index");
        }
    }

